package dsa.bitManupulation;

public class Xor {

    public static void main(String[] args) {
        System.out.println(1^2); // 01 ^ 10 = 11
        System.out.println(1^0); // xor with 0 is always same
        System.out.println(1^1); // same number xor is 0

        System.out.println(1<<1);
    }
}
